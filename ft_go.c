#include "fdf.h"

void				ft_go(t_fdf *fdf, int key)
{
	if (key == 123)
		fdf->go_x -= 5;
	if (key == 124)
		fdf->go_x += 5;
	if (key == 125)
		fdf->go_y += 5;
	if (key == 126)
		fdf->go_y -= 5;
}
